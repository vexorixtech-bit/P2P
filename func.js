// ===== Language State =====
// Get saved language from localStorage or default to Tamil
let currentLang = 'ta';
try { currentLang = localStorage.getItem('lang') || 'ta'; } catch (e) {}

// ===== Apply Language to Page =====
// Translates all data-i18n elements, swaps logo, updates button text,
// and sets lang class on <html> for CSS targeting
function applyLanguage(lang) {

  // Translate all elements with data-i18n attribute
  const els = document.querySelectorAll('[data-i18n]');

  els.forEach(el => {
    const key = el.dataset.i18n;

    if (i18n[lang] && i18n[lang][key]) {
      if (el.tagName === 'TITLE') {
        el.textContent = i18n[lang][key];
      } else {
        el.innerHTML = i18n[lang][key];
      }
    }
  });

  // Swap logo image based on language
  const heroImage = document.getElementById("heroImage");

  const logo = document.getElementById("headerLogo");
  if (logo) {
    logo.src = 'images/logo.png';
  }

  // Update language toggle button text
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.innerHTML = lang === 'ta' ?  'English' : '<span style="font-size:17px">தமிழ்</span>';
  });

  // Set lang attribute and class on <html> for font/layout CSS rules
  document.documentElement.lang = lang;
  document.documentElement.classList.remove('lang-ta', 'lang-en');
  document.documentElement.classList.add('lang-' + lang);
  try { localStorage.setItem('lang', lang); } catch (e) {}
  currentLang = lang;

}
// ===== Init on DOM Ready =====
document.addEventListener('DOMContentLoaded', function () {
  window.scrollTo(0, 0);
  applyLanguage(currentLang);
});

// ===== Single delegated click handler (nav + language toggle) =====
document.addEventListener('click', function (e) {
  var nav = document.getElementById('navbar');

  var langBtn = e.target.closest('.lang-btn');
  if (langBtn) {
    e.preventDefault();
    applyLanguage(currentLang === 'ta' ? 'en' : 'ta');
    if (nav) nav.classList.remove('active');
    return;
  }

  if (e.target.closest('#navbar a')) {
    if (nav) nav.classList.remove('active');
  }
});